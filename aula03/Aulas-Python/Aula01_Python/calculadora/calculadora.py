num1 = float(input("Digite o valor do primeiro numero: \n"))
num2 = float(input("Digite o valor do segundo numero: \n"))

soma = num1 + num2
sub = num1 - num2
prod = num1 * num2
div = num1 / num2
print(f"A soma dos numeros: {num1} e {num2} é igual a {soma} \n")
print(f"A subtraçâo dos numeros: {num1} e {num2} é igual a {sub} \n")
print(f"O produto dos numeros: {num1} e {num2} é igual a {prod} \n")
print(f"A divisão dos numeros: {num1} e {num2} é igual a {div} \n")